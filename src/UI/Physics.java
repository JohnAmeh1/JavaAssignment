package UI;

public class Physics {
    float multiply(float num1,float num2){
        return num1 * num2;
    }
    float multiply1(float num1,float num2,float num3){
        return num1 * num2 * num3;
    }
    float Divide(float num1,float num2){
        return num1/num2;
    }
    float Divide2(float num1,float num2) {
        return num1*(num2 * 2) / 0.5f;
    }
    float Addition(float num1,float num2,float num3,float num4){
        return num1+num2+num3+num4;
    }
    float Addition1(float num1,float num2,float num3){
        return num1+num2+num3;
    }
}
